package MiniProject.Gomoku;

public class Board {


    /**===============================================================================**/
    /**================================ private field ================================**/
    /**===============================================================================**/


    private String[][] blocks;


    /**=============================================================================**/
    /**================================ constructor ================================**/
    /**=============================================================================**/


    public Board() { initBoard(); }


    /**===============================================================================**/
    /**================================ public method ================================**/
    /**===============================================================================**/


    public void printBoard() {}


    /**================================================================================**/
    /**================================ private method ================================**/
    /**================================================================================**/


    private void initBoard() {
        this.blocks = new String[15][15];
        for( int i=0; i < 15; i++ )
            for( int j=0; j < 15; j++ )
                this.blocks[i][j] = "+";
    }


}