package MiniProject.Gomoku.Enum;

public enum Prompt {


    START_GAME( "Start a new Game ( yes / no ) : " ),
    PUT_PIECE( "Player using # turns to put a piece at : " ),
    ;


    /**===============================================================================**/
    /**================================ private field ================================**/
    /**===============================================================================**/


    private final String message;


    /**=============================================================================**/
    /**================================ constructor ================================**/
    /**=============================================================================**/


    private Prompt( String message ) { this.message = message; }


    /**===============================================================================**/
    /**================================ public method ================================**/
    /**===============================================================================**/


    public String getMessage() { return this.message; }
}
